def divide_numbers():
    try:
        # Запрашиваем у пользователя ввод двух чисел
        num1 = float(input("Введите первое число: "))
        num2 = float(input("Введите второе число: "))

        # Пытаемся разделить первое число на второе
        result = num1 / num2

    except ValueError:
        # Обрабатываем ошибку, если введено нечисловое значение
        print("Ошибка: введено нечисловое значение.")

    except ZeroDivisionError:
        # Обрабатываем ошибку, если второе число равно нулю
        print("Ошибка: деление на ноль невозможно.")

    else:
        # В случае успешного выполнения выводим результат
        print(f"Результат деления: {result}")

    finally:
        # Сообщение о завершении работы программы
        print("Программа завершена.")

# Запуск программы
divide_numbers()
